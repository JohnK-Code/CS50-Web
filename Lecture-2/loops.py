for i in [0,1,2,3,4,5]: # basic syntax for a python loop using a list
    print(i)

for i in range(6): # same as above but uses range function
    print(i)

names = ["Harry", "Ron", "Hermione"] # list containing strings
for name in names: # loops through list items and prints each item
    print(name)

name = "Harry" # string
for character in name: # loops through each element in name and prints it
    print(character)
